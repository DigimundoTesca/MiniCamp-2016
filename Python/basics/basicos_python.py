'''
Operaciones sencillas
'''
# este es un comentario de una sola linea

var1 = 98
var2 = 5

# Suma
result = var1 + var2
print('Suma:', result)

# Resta
result = var1 - var2
mensaje = 'La resta es: {}'.format(result)
print(mensaje)

# Multiplicación
result = var1 * var2
print('Multiplicación: {}'.format(result))

# división (Tradicional)
result = var1 / var2
mi_mensaje = 'La division es: %s' % (result)
print(mi_mensaje)

# división (Enteros)
result = var1 // var2
mensaje = 'La division en enteros es: %s' % (result)
print(mensaje)

#Potenciación
result = var1 ** var2
